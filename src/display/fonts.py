def get_char_bitmap(char, font=FONT_5X7):
    """
    Get bitmap data for a character.

    Args:
        char: Character to look up
        font: Font dictionary to use

    Returns:
        List of byte values (rows), or None if char not found
    """
    chars = font.get('chars', {})
    return chars.get(char, chars.get(' '))  # Default to space


def get_text_width(text, font=FONT_5X7, spacing=1):
    """
    Calculate pixel width of text string.

    Args:
        text: Text to measure
        font: Font to use
        spacing: Pixels between characters

    Returns:
        Width in pixels
    """
    if not text:
        return 0

    char_width = font.get('width', 5)
    return len(text) * char_width + (len(text) - 1) * spacing


def get_font_height(font=FONT_5X7):
    """Get height of font in pixels."""
    return font.get('height', 7)


